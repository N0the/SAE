        <h1>Comment nos potions sont-elles concoctées ?</h1>

        <img src="public/images/etapes_conception_biere.png" alt="schéma des étapes de conception des potions-bières" class="schema">

        <ol> 
            <li><strong>CONCASSAGE</strong> = faciliter la conversion de l’amidon en sucre simple en broyant les céréales maltées.</li><br>
            <li><strong>EMPÂTAGE</strong> = l’étape la plus importante du brassage !</li><br>
            <li><strong>ÉBULITION</strong> = introduction des houblons, une des matières premières de la bière, et qui va apporter de l’amertume mais surtout de nombreux arômes.</li><br>
            <li><strong>FERMENTATION</strong> = qui intervient après le processus du brassage durant laquelle le moût va être ensemencé grâce aux levures qui vont transformer les sucres en alcool et en CO2.</li><br>
            <li><strong>EMBOUTEILLAGE</strong> = transvasement du breuvage dans nos fioles.</li>
        </ol>    
            
